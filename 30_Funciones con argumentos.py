# Definimos una función que suma dos números
def sumar(a, b):
    return a + b  # Devolvemos la suma de los dos números

# Llamamos a la función para sumar dos números
resultado = sumar(3, 5)  # Pasamos los argumentos 3 y 5 a la función
print(f"La suma es: {resultado}")  # Imprimimos el resultado de la suma
