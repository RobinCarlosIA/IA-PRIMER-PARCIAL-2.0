# Declaramos la variable 'h' con un saludo en minúsculas
h = "hola bro"
# Usamos el método title() para convertir la primera letra de cada palabra en mayúscula
print(h.title())  # Esto imprimirá: "Hola Bro"

# Declaramos la variable 'a' con una pregunta en minúsculas
a = "como estas?"
# Usamos el método upper() para convertir todo el texto a mayúsculas
print(a.upper())  # Esto imprimirá: "COMO ESTAS?"

# Declaramos la variable 'b' con un mensaje en mayúsculas
b = "BIEN GRACIAS"
# Usamos el método lower() para convertir todo el texto a minúsculas
print(b.lower())  # Esto imprimirá: "bien gracias"




