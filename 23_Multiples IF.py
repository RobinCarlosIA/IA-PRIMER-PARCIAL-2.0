
numero = int(input("Ingresa un número: "))

# Verificamos si el número es positivo
if numero > 0:
    print("El número es positivo.")

# Verificamos si el número es negativo
if numero < 0:
    print("El número es negativo.")

# Verificamos si el número es cero
if numero == 0:
    print("El número es cero.")

# Verificamos si el número es par
if numero % 2 == 0:
    print("El número es par.")
    
# Verificamos si el número es impar
if numero % 2 != 0:
    print("El número es impar.")

# Verificamos si el número es mayor que 10
if numero > 10:
    print("El número es mayor que 10.")

# Verificamos si el número es menor que 10
if numero < 10:
    print("El número es menor que 10.")

# Verificamos si el número es igual a 10
if numero == 10:
    print("El número es igual a 10.")
